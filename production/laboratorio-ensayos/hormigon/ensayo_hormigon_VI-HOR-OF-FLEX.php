function VI_Calcula() {
  var VI_Masa = parseFloat(document.getElementById('VI_Masa').value);
  var VI_Volumen = parseFloat(document.getElementById('VI_Volumen').value);
  var VI_DensidadAparente = parseFloat(document.getElementById('VI_DensidadAparente').value);
  var VI_CargaPKN = parseFloat(document.getElementById('VI_CargaPKN').value);
  var VI_CargaTipo = parseFloat(document.getElementById('VI_CargaTipo').value);

  var VI_b1 = parseFloat(document.getElementById('VI_b1').value);
  var VI_b2 = parseFloat(document.getElementById('VI_b2').value);
  var VI_b3 = parseFloat(document.getElementById('VI_b3').value);

  var VI_h1 = parseFloat(document.getElementById('VI_h1').value);
  var VI_h2 = parseFloat(document.getElementById('VI_h2').value);
  var VI_h3 = parseFloat(document.getElementById('VI_h3').value);

  var VI_l1 = parseFloat(document.getElementById('VI_l1').value);
  var VI_l2 = parseFloat(document.getElementById('VI_l2').value);

  var VI_Luz = document.getElementById('VI_Luz').value;
  var VI_a = document.getElementById('VI_a').value;


  VI_b = (VI_b1 + VI_b2 + VI_b3) / 3;
  VI_h = (parseFloat(VI_h1) + parseFloat(VI_h2) + parseFloat(VI_h3)) / 3;
  VI_AnchoMedio = VI_b;
  VI_AlturaMedio = VI_h;
  VI_l = (parseFloat(VI_l1) + parseFloat(VI_l2)) / 2;
  VI_Volumen = Math.round(VI_b * VI_h * VI_l * 100)/100;
  VI_DensidadAparente = Math.round(1000000000 * parseFloat(VI_Masa) / VI_Volumen * 100)/100;
  if (parseFloat(VI_a)==0){
    VI_Resistencia = Math.round(((1000 * VI_CargaPKN * parseFloat(VI_Luz)) / (parseFloat(VI_AnchoMedio) * Math.pow(parseFloat(VI_AlturaMedio),2))) * 100)/100
  }else{
      if ((parseFloat(VI_a)>0)&&(parseFloat(VI_a)<(0.05 * parseFloat(VI_Luz)))){
  	     VI_Resistencia = Math.round(((3 * 1000 * VI_CargaPKN * parseFloat(VI_Luz)) / (parseFloat(VI_AnchoMedio) * Math.pow(parseFloat(VI_AlturaMedio),2))) * 100)/100
      }else{
        $("#ModalFueraRango").modal();
     }
   }


   document.getElementById('VI_b').value = parseFloat(VI_b);
   document.getElementById('VI_h').value = parseFloat(VI_h);
   document.getElementById('VI_AnchoMedio').value = parseFloat(VI_AnchoMedio);
   document.getElementById('VI_AlturaMedio').value = parseFloat(VI_AlturaMedio);
   document.getElementById('VI_l').value = parseFloat(VI_l);
   document.getElementById('VI_Volumen').value = parseFloat(VI_Volumen);
   document.getElementById('VI_DensidadAparente').value = parseFloat(VI_DensidadAparente);
   document.getElementById('VI_Resistencia').value = parseFloat(VI_Resistencia);
}

</script>
